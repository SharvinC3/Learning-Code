function sharvinfunction() {
    const x = document.getElementById('sharvin')

    if(x.style.display == 'block') { 
        x.style = 'display:none;'

    }
    else {
        x.style = 'display:block;'
        x.innerHTML = 'welcome to the secret site'
    }

}

